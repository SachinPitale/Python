#slicing
##slice out substrings,sublists, subttuples using indexes 

x = "computer"
print (x[1:4])  # item 1 to 3
print (x[1:6:2]) # items 1,3,5,
print (x[3:])   #items 3 to send
print (x[:5])  #items 0 to 4
print (x[-1])  # last item
print (x[-3:]) #last 3 item
print (x[:-2]) #All except last 2 items


